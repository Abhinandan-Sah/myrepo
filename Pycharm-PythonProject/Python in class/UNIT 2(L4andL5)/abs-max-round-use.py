#function that can be accessed by import math function only
import math
x=12.34
y=6
print(math.floor(x)) #it return value 12
print(math.ceil(x))  #it will return value 13
print(3/2) #quotent
print(math.fmod(x,y)) #give remainder
print(math.trunc(x))  #give integer value
print(math.factorial(5))  #give factorial
print(math.gcd(y))
print(math.copysign(x,y))
print(math.fsum(34,6,10))
print(math.exp(2))
print(math.sin(30))  # it will print in radian
print(math.radians(y))
print(math.degree(y))
print(math.sin(math.radians(30))) #degree
z=10
print(math.hypot(y,z)) #sqrt(x^2+y^2)

